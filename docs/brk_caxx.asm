nc3000 中断执行地址计算方式：
例如 $014a, 01表示程序是从01页执行
若像这样的中断的前两个数是00-19的计算方法：首先根据前两个数字转到指定页，这里是01页，然后用$4000+$4ax2，$4001+$4ax2，这样就算出了执行代码所存放的地址，根据$4000+$4ax2、$4001+$4ax2地址中的值跳转到指定位置就行了
像$cxxx就不是这样算了。
计算方法：比如INT $CA12，首先转到$c000地址，让IO $0a的值等于第二个数字A，再根据$C000+12*2、$C001+12*2地址中的值跳转到指定位置就行了


